# Using `in` membership operator
my_list = [1, 2, 3, 4, 5]

# Check if 3 is present in the list
if 3 in my_list:
    print("3 is present in the list")
else:
    print("3 is not present in the list")

# Check if 6 is present in the list
if 6 in my_list:
    print("6 is present in the list")
else:
    print("6 is not present in the list")

# Using `not in` membership operator
my_string = "Hello, world!"

# Check if 'H' is not present in the string
if 'H' not in my_string:
    print("'H' is not present in the string")
else:
    print("'H' is present in the string")

# Check if 'z' is not present in the string
if 'z' not in my_string:
    print("'z' is not present in the string")
else:
    print("'z' is present in the string")
