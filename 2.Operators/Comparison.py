# Define two variables
num1 = 10
num2 = 5

# Equal to
print("Equal to:")
print(num1 == num2)  # False
print(num1 == 10)    # True

# Not equal to
print("\nNot equal to:")
print(num1 != num2)  # True
print(num2 != 5)     # False

# Greater than
print("\nGreater than:")
print(num1 > num2)   # True
print(num2 > 10)     # False

# Less than
print("\nLess than:")
print(num1 < num2)   # False
print(num1 < 15)     # True

# Greater than or equal to
print("\nGreater than or equal to:")
print(num1 >= num2)  # True
print(num2 >= 5)     # True
print(num2 >= 10)    # False

# Less than or equal to
print("\nLess than or equal to:")
print(num1 <= num2)  # False
print(num2 <= 5)     # True
print(num1 <= 10)    # True
