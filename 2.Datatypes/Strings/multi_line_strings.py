# Using triple quotes for multi-line strings
multi_line_string = """This is a 
multi-line 
string."""
print(multi_line_string)
# Output:
# This is a
# multi-line
# string.

# Alternative using triple single quotes for multi-line strings
alternative_multi_line_string = '''Another 
multi-line 
string.'''
print(alternative_multi_line_string)
# Output:
# Another
# multi-line
# string.
