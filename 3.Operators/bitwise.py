# Bitwise AND (&)
a = 5  # 101 in binary
b = 3  # 011 in binary
result_and = a & b
print("Bitwise AND result:", result_and)  # Output: 1

# Bitwise OR (|)
result_or = a | b
print("Bitwise OR result:", result_or)  # Output: 7

# Bitwise XOR (^)
result_xor = a ^ b
print("Bitwise XOR result:", result_xor)  # Output: 6

# Bitwise NOT (~)
result_not_a = ~a
print("Bitwise NOT result for a:", result_not_a)  # Output: -6

# Left shift (<<)
result_left_shift = a << 1
print("Left shift result for a:", result_left_shift)  # Output: 10

# Right shift (>>)
result_right_shift = a >> 1
print("Right shift result for a:", result_right_shift)  # Output: 2
