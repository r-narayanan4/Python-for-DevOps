# Tuple Functions Demo

# 1. len(tuple)
# Define a tuple
my_tuple = (1, 2, 3, 4, 5)
# Calculate the length of the tuple
tuple_length = len(my_tuple)
# Print the length of the tuple
print("Length of the tuple:", tuple_length)  # Output: 5

# 2. sorted(tuple)
# Define a tuple
my_tuple = (5, 2, 3, 1, 4)
# Sort the tuple
sorted_tuple = sorted(my_tuple)
# Print the sorted tuple
print("Sorted tuple:", sorted_tuple)  # Output: [1, 2, 3, 4, 5]

# 3. max(tuple)
# Define a tuple
my_tuple = (5, 2, 3, 1, 4)
# Find the maximum element in the tuple
max_element = max(my_tuple)
# Print the maximum element
print("Maximum element in the tuple:", max_element)  # Output: 5

# 4. min(tuple)
# Define a tuple
my_tuple = (5, 2, 3, 1, 4)
# Find the minimum element in the tuple
min_element = min(my_tuple)
# Print the minimum element
print("Minimum element in the tuple:", min_element)  # Output: 1

# 5. sum(tuple)
# Define a tuple
my_tuple = (1, 2, 3, 4, 5)
# Calculate the sum of elements in the tuple
sum_of_elements = sum(my_tuple)
# Print the sum of elements
print("Sum of elements in the tuple:", sum_of_elements)  # Output: 15
