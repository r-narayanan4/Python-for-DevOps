# looping_set.py

# Creating a Set
my_set = {1, 2, 3}

# Looping over elements of the set
print("Elements of the set:")
for element in my_set:
    print(element)
# Output:
# Elements of the set:
# 1
# 2
# 3

# Example 1: Iterating over a Set of Strings
# Creating a Set of Strings
words = {"apple", "banana", "cherry"}

# Looping over elements of the set of strings
print("\nWords in the set:")
for word in words:
    print(word)
# Output:
# Words in the set:
# cherry
# banana
# apple

# Example 2: Iterating over a Set of Tuples
# Creating a Set of Tuples
coordinates = {(1, 2), (3, 4), (5, 6)}

# Looping over elements of the set of tuples
print("\nCoordinates in the set:")
for coord in coordinates:
    print(coord)
# Output:
# Coordinates in the set:
# (3, 4)
# (1, 2)
# (5, 6)
