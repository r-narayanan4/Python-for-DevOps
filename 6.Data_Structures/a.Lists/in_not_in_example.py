# List of numbers
numbers = [1, 2, 3, 4, 5]

# Check if 3 is in the list
print("Is 3 in the list?", 3 in numbers)  # Output: True

# Check if 6 is not in the list
print("Is 6 not in the list?", 6 not in numbers)  # Output: True

# Check if 0 is in the list
print("Is 0 in the list?", 0 in numbers)  # Output: False

# Check if 5 is not in the list
print("Is 5 not in the list?", 5 not in numbers)  # Output: False

# String
sentence = "The quick brown fox jumps over the lazy dog"

# Check if "fox" is in the sentence
print("Is 'fox' in the sentence?", "fox" in sentence)  # Output: True

# Check if "cat" is not in the sentence
print("Is 'cat' not in the sentence?", "cat" not in sentence)  # Output: True

# Check if "dog" is in the sentence
print("Is 'dog' in the sentence?", "dog" in sentence)  # Output: True

# Check if "elephant" is not in the sentence
print("Is 'elephant' not in the sentence?", "elephant" not in sentence)  # Output: True
