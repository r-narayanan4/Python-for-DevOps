# Building loops with while, for, range(), and in

# Using for loop with range() function
print("Using for loop with range()")
for i in range(5):
    print(i)

# Using while loop
print("\nUsing while loop")
count = 0
while count < 5:
    print(count)
    count += 1

# Using for loop with in keyword to iterate through a sequence
print("\nUsing for loop with in keyword")
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)
